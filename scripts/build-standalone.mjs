import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'

const distDir = 'dist'
const outFile = 'standalone.html'
const indexPath = join(distDir, 'index.html')

let html = readFileSync(indexPath, 'utf8')

html = html.replace(/<link rel="stylesheet" crossorigin href="([^"]+)">/g, (_tag, href) => {
  const assetPath = join(distDir, href.replace(/^\//, ''))
  const css = readFileSync(assetPath, 'utf8')
  return `<style>\n${css}\n</style>`
})

html = html.replace(/<script type="module" crossorigin src="([^"]+)"><\/script>/g, (_tag, src) => {
  const assetPath = join(distDir, src.replace(/^\//, ''))
  const js = readFileSync(assetPath, 'utf8')
  return `<script type="module">\n${js}\n</script>`
})

const generated = `<!-- Generated by npm run standalone. Do not edit by hand. -->\n${html}`
mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, generated)
console.log(`Wrote ${outFile}`)
